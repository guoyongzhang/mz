# create a function named area, with one parameter radius.  The function returns the area if the circle.
def circleArea(radius):
    return 3.14 * pow(radius, 2)

# create a function called volume, with one parameter radius.  It returns the volume of such a sphere.
def sphereVolume(radius):
    return 1.33 * 3.14 * pow(radius, 3)

print(circleArea(2))
print(circleArea(3))
print(circleArea(4))
print(circleArea(5))

print(sphereVolume(2))
print(sphereVolume(3))
print(sphereVolume(4))

